package Eje_7Linea;

public class Punto {

	protected int xA;
	protected int yA;
	protected int xB;
	protected int yB;
	
	Punto(){
		this.xA=5;
		this.yA=8;
		this.xB=20;
		this.yB=3;
	}
	
	public void Punto(int xA,int yA,int xB,int yB){
		this.xA=xA;
		this.yA=yA;
		this.xB=xB;
		this.yB=yB;
	}
}
